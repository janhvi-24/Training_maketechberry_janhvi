public class operators{
    public static void main(String args[]){
        int n1 = 45, n2 = 89;

        System.out.println("Arithmetic Operators:");
        System.out.println("n1+n2 = "+(n1+n2));
        System.out.println("n1-n2 = "+(n1-n2));
        System.out.println("n1*n2 = "+(n1*n2));
        System.out.println("n1/n2 = "+(n1/n2));
        System.out.println("n1%n2 = "+(n1%n2));
    }
}